public final class voidDoubleIntString {
	public static void mv() {
		java.lang.System.out.println("3");
	}

	public static int mi() {
		java.lang.System.out.println("4");
		return 5;
	}

	public static double md() {
		return 6;
	}

	public static java.lang.String ms() {
		return "7ztr";
	}

	public static void main(final java.lang.String[] args) {
		mv();
		mi();
		java.lang.System.out.println(mi());
		java.lang.System.out.println(md());
		java.lang.System.out.println(ms());
	}
}